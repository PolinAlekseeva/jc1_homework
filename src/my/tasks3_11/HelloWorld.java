package my.tasks3_11;

public class HelloWorld {
    public static void main(String[] args) {
        byte b1 = 0x55;
        System.out.println("byte b = " + b1 + ";");
        short s1 = 0x55ff;
        System.out.println("short s = " + s1 + ";");
        int i1 = 1000000;
        System.out.println("int i = " + i1 + ";");
        long l1 = 0xffffffffL;
        System.out.println("long l = " + l1 + ";");
        char c1 = 'a';
        System.out.println("char c = " + c1 + ";");
        float f1 = .25f;
        System.out.println("float f = " + f1 + ";");
        double d1 = .00001234;
        System.out.println("double d = " + d1 + ";");
        boolean bool1 = true;
        System.out.println("boolean bool = " + bool1 + ";");

    }
}
